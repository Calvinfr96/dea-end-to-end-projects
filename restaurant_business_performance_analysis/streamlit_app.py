season_order = ['Winter', 'Spring', 'Summer', 'Fall']
seasonal_revenue_trend_df["SEASON"] = pd.Categorical(
    seasonal_revenue_trend_df["SEASON"], 
    categories=season_order, 
    ordered=True
)

