price_effectiveness_query = f"""
    WITH item_price_buckets AS (
        SELECT
            year,
            month,
            ITEM_PRICE,
            ITEM_QUANTITY,
            OPTION_PRICE,
            OPTION_QUANTITY,
            NTILE(10) OVER(PARTITION BY year, month ORDER BY ITEM_PRICE) AS PRICE_BUCKET
        FROM gold_sales
    )

    SELECT
        PRICE_BUCKET,
        SUM((ITEM_PRICE * ITEM_QUANTITY) +
        (OPTION_PRICE * OPTION_QUANTITY)) AS BUCKET_REVENUE
    FROM item_price_buckets
    WHERE year = ? AND month = ?
    GROUP BY PRICE_BUCKET
    ORDER BY BUCKET_REVENUE;
"""
price_effectiveness_df = con.execute(price_effectiveness_query, [st.session_state.year, st.session_state.month]).df()

