seasonal_revenue_trend_query = """
    WITH seasonal_revenue AS (
        SELECT
            year,
            month,
            ITEM_PRICE,
            ITEM_QUANTITY,
            OPTION_PRICE,
            OPTION_QUANTITY,
            (
                CASE
                    WHEN month <= 3 THEN 'Winter'
                    WHEN month <= 6 THEN 'Spring'
                    WHEN month <= 9 THEN 'Summer'
                    ELSE 'Fall'
                END
            ) AS SEASON
        FROM gold_sales
    )

    SELECT
        SEASON,
        SUM((ITEM_PRICE * ITEM_QUANTITY) +
        (OPTION_PRICE * OPTION_QUANTITY)) AS SEASONAL_REVENUE
    FROM seasonal_revenue
    WHERE year = ?
    GROUP BY SEASON
"""
