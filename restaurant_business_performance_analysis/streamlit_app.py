# 2. Instantiate DuckDB memory engine with secure S3 configurations
@st.cache_resource
def get_duckdb_connection():
    con = duckdb.connect()
    con.execute("INSTALL httpfs; LOAD httpfs;")
    # Pass our credentials into the DuckDB network extension handler
    con.execute(f"SET s3_region='{aws_region}';")
    con.execute(f"SET s3_access_key_id='{aws_key}';")
    con.execute(f"SET s3_secret_access_key='{aws_secret}';")
    
    # Map the entire S3 directory containing your Snappy-Parquet files as a view
    # Using the wildcard ensures all 4 partition files generated by Spark are read as one table
    s3_path = "s3://restaurant-analysis-calvinfr/gold/sales_performance/*.parquet"
    con.execute(f"CREATE OR REPLACE VIEW gold_sales AS SELECT * FROM read_parquet('{s3_path}')")
    return con

